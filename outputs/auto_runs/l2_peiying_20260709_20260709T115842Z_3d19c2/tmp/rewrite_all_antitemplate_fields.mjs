let peiyingPlanUpdated = false;
try {
  const peiyingPlan = JSON.parse(await fs.readFile(peiyingPlanPath, "utf8"));
  for (const row of peiyingPlan.rows) {
    const sheetRow = Number(row.sheetRow);
    const updated = updatedRows.find((item) => item.row === sheetRow);
    if (!updated) continue;
    const fieldsByIndex = new Map([
      [0, updated.question],
      [5, updated.summary],
      [10, updated.attachmentContent],
      [12, updated.productContent],
      [13, updated.steps],
    ]);
    for (const [index, value] of fieldsByIndex.entries()) {
      if (row.updates[index]) row.updates[index].value = value;
    }
  }
  await fs.writeFile(peiyingPlanPath, `${JSON.stringify(peiyingPlan, null, 2)}\n`, "utf8");
  peiyingPlanUpdated = true;
} catch (error) {
  peiyingPlanUpdated = false;
}

