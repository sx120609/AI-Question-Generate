const chineseOrdinals = ["一", "二", "三", "四", "五", "六", "七", "八"];

function enumerateAttachmentContent(value) {
  let index = 0;
  return String(value ?? "")
    .split("\n")
    .map((line) => {
      if (!line.startsWith("- ")) return line;
      const label = chineseOrdinals[index] || String(index + 1);
      index += 1;
      return `- 附件${label}：${line.slice(2)}`;
    })
    .join("\n");
}

