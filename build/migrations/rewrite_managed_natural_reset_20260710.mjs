function normalize(value = "") {
  return String(value ?? "").replace(/\r\n/g, "\n").trim();
}

function visibleLength(value) {
  return [...normalize(value).replace(/\s+/gu, "")].length;
}

function sanitizeNarrative(value, { attachment = false } = {}) {
  let text = normalize(value)
    .replace(/([\u4e00-\u9fff])\s+(\d)/gu, "$1$2")
    .replace(/(?<![A-Za-z])(\d)\s+([\u4e00-\u9fff])/gu, "$1$2");
  if (!attachment) {
    text = text.replace(/\bdocx\b/giu, "Word").replace(/\bxlsx\b/giu, "Excel");
  }
  return text;
}

function attachmentParts(value) {
  const text = normalize(value)
    .replace(/^附件用途和边界如下：\s*/u, "")
    .replace(/^以下为[^\n]*附件[^\n]*\n+/u, "");
  const itemPattern = /(?:^|\n)-?\s*(?=附件(?:[一二三四五六七八九十]+|\d+)[：:])/gu;
  const starts = [...text.matchAll(itemPattern)].map((match) => match.index + (match[0].startsWith("\n") ? 1 : 0));
  if (!starts.length) return [text];
  const tailPattern = /\n(?:缺口|资料缺口|缺口边界|总边界|未随附件提供的材料)[：:]/gu;
  const tailStart = [...text.matchAll(tailPattern)].at(-1)?.index ?? text.length;
  return starts
    .filter((start) => start < tailStart)
    .map((start, index, active) => text.slice(start, Math.min(active[index + 1] ?? tailStart, tailStart)).trim().replace(/^-\s*/u, ""));
}

function rewriteAttachmentContent(record, note) {
  const items = attachmentParts(record.附件内容).map((item) => item
    .replace(/证据边界[：:]/gu, "使用时注意：")
    .replace(/请(?=简述|填写|说明|提供|提交|核对)/gu, "需")
    .trim());
  return sanitizeNarrative(`${items.join("\n\n")}\n\n${note}`, { attachment: true });
}

function formatSteps(lines, uid) {
  if (!Array.isArray(lines) || lines.length < 8 || lines.length > 15) {
    throw new Error(`${uid} has ${lines?.length ?? 0} steps; expected 8-15.`);
  }
  return sanitizeNarrative(lines.map((line, index) => `${index + 1}. ${normalize(line)}`).join("\n"));
}

function tsvCell(value) {
  return normalize(value).replace(/\t/g, " ").replace(/\n/g, "\\n");
}

function toTsv(rows) {
  return `${COLUMN_FIELDS.join("\t")}\n${rows.map((row) => COLUMN_FIELDS.map((field) => tsvCell(row[field])).join("\t")).join("\n")}\n`;
}

function splitAttachmentNames(value) {
  return normalize(value).split(/[；;\n]+/u).map((item) => item.trim()).filter(Boolean);
}

async function sha256(filePath) {
  return crypto.createHash("sha256").update(await fs.readFile(filePath)).digest("hex");
}

async function copyAttachments(rows, sourceRunDir, runDir) {
  const copied = [];
  for (const row of rows) {
    const targetDir = path.join(runDir, "attachments", row.UID);
    await fs.mkdir(targetDir, { recursive: true });
    for (const fileName of splitAttachmentNames(row.相关附件)) {
      const sourcePath = path.join(sourceRunDir, "attachments", row.UID, fileName);
      const targetPath = path.join(targetDir, fileName);
      await fs.access(sourcePath);
      await fs.copyFile(sourcePath, targetPath);
      copied.push({ uid: row.UID, fileName, sourcePath, targetPath, sha256: await sha256(targetPath) });
    }
  }
  return copied;
}

function validateRewrite(row, {
  hardMinimum = 560,
  hardMaximum = 1500,
} = {}) {
  const questionLength = visibleLength(row.题目);
  if (questionLength < hardMinimum || questionLength > hardMaximum) {
    throw new Error(`${row.UID} question length ${questionLength} is outside ${hardMinimum}-${hardMaximum}.`);
  }
  if (/\b(?:docx|xlsx)\b/iu.test(`${row.题目}\n${row.任务概括}\n${row.产物内容}\n${row.做题关键步骤}`)) {
    throw new Error(`${row.UID} exposes raw office extensions in narrative fields.`);
  }
  if (/证据拓扑|阶段门|情景模型|监测看板|诊断报告|条款矩阵/iu.test(`${row.题目}\n${row.任务概括}\n${row.产物内容}`)) {
    throw new Error(`${row.UID} contains a retired structure-passport term.`);
  }
  assertNoPoliteImperative(row, {
    label: row.UID,
    fields: ["题目", "任务概括", "附件内容", "产物内容", "做题关键步骤"],
  });
  assertSingleParagraphQuestion(row.题目, { label: row.UID });
  assertClearQuestionRequest(row.题目, { label: row.UID, productFormats: row.产物格式 });
  return { uid: row.UID, sheetRow: row.sheetRow, questionVisibleCharacters: questionLength };
}

