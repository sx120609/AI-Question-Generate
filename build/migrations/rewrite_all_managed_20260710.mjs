function normalize(value = "") {
  return String(value ?? "").replace(/\r\n/g, "\n").trim();
}

function sanitizeNarrative(value, { question = false, humanizeFormats = true } = {}) {
  let text = normalize(value)
    .replace(/([\u4e00-\u9fff])\s+(\d)/gu, "$1$2")
    .replace(/(?<![A-Za-z])(\d)\s+([\u4e00-\u9fff])/gu, "$1$2");
  if (humanizeFormats) {
    text = text.replace(/\bdocx\b/giu, "Word").replace(/\bxlsx\b/giu, "Excel");
  }
  if (question) {
    text = text
      .replace(/不要只/gu, "结论不只")
      .replace(/不得(?=(?:写成|编造|猜|直接|输出|使用|把|替|只|堆))/gu, "不能")
      .replace(/不要(?=(?:写成|编造|猜|直接|输出|使用|把|替|堆))/gu, "避免")
      .replace(/严禁(?=(?:写成|编造|猜|直接|输出|使用|把|替|只|堆))/gu, "避免")
      .replace(/必须(?=(?:问|列出|写|输出|生成|整理|找|说明))/gu, "需要");
  }
  return text;
}

function attachmentParts(value) {
  const text = normalize(value).replace(/^附件用途和边界如下：\s*/u, "");
  const itemPattern = /(?:^|\n)-?\s*(?=附件(?:[一二三四五六七八九十]+|\d+)[：:])/gu;
  const starts = [...text.matchAll(itemPattern)].map((match) => match.index + (match[0].startsWith("\n") ? 1 : 0));
  if (!starts.length) return { items: [text], tail: "" };
  const boundaryPattern = /\n(?:缺口|资料缺口|缺口边界|总边界|未随附件提供的材料)[：:]/gu;
  const boundaries = [...text.matchAll(boundaryPattern)];
  const boundaryStart = boundaries.at(-1)?.index ?? text.length;
  const items = starts
    .filter((start) => start < boundaryStart)
    .map((start, index, active) => text.slice(start, Math.min(active[index + 1] ?? boundaryStart, boundaryStart)).trim().replace(/^-\s*/u, ""));
  return { items, tail: text.slice(boundaryStart).trim() };
}

function rewriteAttachmentContent(record, config) {
  const { items } = attachmentParts(record.附件内容);
  const order = config.order?.length
    ? config.order.map((index) => items[index]).filter(Boolean)
    : config.reverse
      ? [...items].reverse()
      : [...items.slice(config.rotate ?? 0), ...items.slice(0, config.rotate ?? 0)];
  const styled = order.map((item, index) => {
    if (config.style === "cards") return `证据卡${index + 1}｜${item}`;
    if (config.style === "blocks") return `资料块${String.fromCharCode(65 + index)}\n${item}`;
    if (config.style === "questions") return `核对问题${index + 1}\n${item}`;
    return item;
  }).join(config.style === "plain" ? "\n" : "\n\n");
  let result = `${config.intro}\n${styled}\n${config.boundary}`;
  for (const [pattern, replacement] of config.replacements ?? []) {
    result = result.replaceAll(pattern, replacement);
  }
  return sanitizeNarrative(result, { humanizeFormats: false });
}

function steps(lines, uid) {
  const completed = [...lines, ...(STEP_COMPLETIONS[uid] ?? [])];
  if (completed.length < 8 || completed.length > 15) {
    throw new Error(`${uid} has invalid step count ${completed.length}; expected 8-15.`);
  }
  return sanitizeNarrative(completed.map((line, index) => `${index + 1}. ${line}`).join("\n"));
}

function tsvCell(value) {
  return normalize(value).replace(/\t/g, " ").replace(/\n/g, "\\n");
}

function toTsv(rows) {
  return `${COLUMN_FIELDS.join("\t")}\n${rows.map((row) => COLUMN_FIELDS.map((field) => tsvCell(row[field])).join("\t")).join("\n")}\n`;
}

function splitAttachmentNames(value) {
  return normalize(value).split(/[；;\n]+/u).map((item) => item.trim()).filter(Boolean);
}

async function sha256(filePath) {
  return crypto.createHash("sha256").update(await fs.readFile(filePath)).digest("hex");
}

async function materializeAttachments(rows, runDir) {
  const index = await findFilesByName(path.join(ROOT, "outputs"));
  const copied = [];
  for (const row of rows) {
    const targetDir = path.join(runDir, "attachments", row.UID);
    await fs.mkdir(targetDir, { recursive: true });
    for (const fileName of splitAttachmentNames(row.相关附件)) {
      const targetPath = path.join(targetDir, fileName);
      const matches = (index.get(fileName) ?? []).filter((candidate) => !path.resolve(candidate).startsWith(path.resolve(runDir)));
      if (!matches.length) throw new Error(`Missing local attachment for ${row.UID}: ${fileName}`);
      const hashes = new Map();
      for (const candidate of matches) hashes.set(candidate, await sha256(candidate));
      if (new Set(hashes.values()).size > 1) {
        const hint = ATTACHMENT_SOURCE_HINTS[row.UID]?.[fileName];
        const hinted = hint ? matches.filter((candidate) => candidate.includes(hint)) : [];
        if (hinted.length !== 1) {
          throw new Error(`Ambiguous attachment with different contents for ${row.UID}: ${fileName}`);
        }
        matches.splice(0, matches.length, hinted[0]);
      }
      await fs.copyFile(matches[0], targetPath);
      copied.push({ uid: row.UID, fileName, sourcePath: matches[0], targetPath, sha256: hashes.get(matches[0]) });
    }
  }
  return copied;
}

function fullColumnMap() {
  return COLUMN_FIELDS.map((field, index) => ({ field, column: String.fromCharCode(65 + index) }));
}

async function buildRun(config) {
  const runDir = path.join(ROOT, "outputs", "auto_runs", config.runId);
  const snapshotPath = path.join(runDir, "sources", "managed_records_before.json");
  const snapshot = JSON.parse(await fs.readFile(snapshotPath, "utf8"));
  const byUid = new Map(snapshot.records.map((row) => [row.UID, row]));
  const rows = config.orderedUids.map((uid, index) => {
    const source = byUid.get(uid);
    const rewrite = REWRITES[uid];
    if (!source || !rewrite) throw new Error(`Missing source or rewrite for ${uid}`);
    const next = {
      ...source,
      题目: formatQuestionAsSingleParagraph(
        sanitizeNarrative(`${rewrite.question}${rewrite.questionTail ? `\n\n${rewrite.questionTail}` : ""}`, { question: true }),
      ),
      任务概括: sanitizeNarrative(rewrite.summary),
      附件内容: rewriteAttachmentContent(source, rewrite.attachments),
      产物内容: sanitizeNarrative(rewrite.product),
      做题关键步骤: steps(rewrite.stepLines, source.UID),
    };
    for (const field of ["题目", "任务概括", "附件内容", "产物内容", "做题关键步骤"]) {
      if (normalize(next[field]) === normalize(source[field])) throw new Error(`${uid} did not rewrite ${field}`);
    }
    assertNoPoliteImperative(next, { label: uid });
    assertSingleParagraphQuestion(next.题目, { label: uid });
    assertClearQuestionRequest(next.题目, { label: uid, productFormats: next.产物格式 });
    return { ...next, __profileIndex: index };
  });

  if (rows.length !== snapshot.count || new Set(rows.map((row) => row.UID)).size !== rows.length) {
    throw new Error(`${config.runId} rewrite coverage mismatch.`);
  }

  const tsvPath = path.join(runDir, "drafts", "l2_questions_rewritten.tsv");
  const planPath = path.join(runDir, "feishu", "feishu_fill_plan_rewrite.json");
  await fs.writeFile(tsvPath, toTsv(rows), "utf8");
  const text = await fs.readFile(tsvPath, "utf8");
  const plan = buildFeishuFillPlan({
    text,
    sourcePath: tsvPath,
    sheetRows: rows.map((row) => row.sheetRow),
    count: rows.length,
    columnMap: fullColumnMap(),
  });
  await writeJsonAtomic(planPath, plan);
  await writeJsonAtomic(path.join(runDir, "sources", "profile_mapping.json"), {
    schemaVersion: 1,
    runId: config.runId,
    rows: rows.map((row, index) => ({ slot: index + 1, profileIndex: index, uid: row.UID, sheetRow: row.sheetRow })),
  });
  const attachments = await materializeAttachments(rows, runDir);
  await writeJsonAtomic(path.join(runDir, "sources", "attachment_manifest.json"), { count: attachments.length, attachments });
  return { runId: config.runId, tsvPath, planPath, rows: rows.map((row) => row.sheetRow), uids: rows.map((row) => row.UID), attachments: attachments.length };
}

async function main() {
  const results = [];
  for (const config of Object.values(RUNS)) results.push(await buildRun(config));
  return { ok: true, results };
}

